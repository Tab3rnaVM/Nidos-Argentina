function renderEvento(eventData) {
  const container = document.getElementById("eventoContainer");

  // Renderizar el título y las fechas
  container.innerHTML = `
      <div class="secciontitle">
        <div class="col-lg-12">
          <div class="heading-section">
            <h4>${eventData.titulo}</h4>
          </div>
        </div>
      </div>
      <div class="col-lg-12" style="margin-top: 15px; padding: 0 !important">
        <div class="sub-secciontitle" style="margin-bottom: 0 !important">
          <p class="subtitles-blancos">Del ${new Date(
            eventData.fechas.inicio
          ).toLocaleDateString("es-ES", {
            day: "2-digit",
            month: "long",
          })} (${new Date(eventData.fechas.inicio).toLocaleTimeString("en-US", {
    hour: "2-digit",
    minute: "2-digit",
  })}) hasta el ${new Date(eventData.fechas.fin).toLocaleDateString("es-ES", {
    day: "2-digit",
    month: "long",
  })} (${new Date(eventData.fechas.fin).toLocaleTimeString("en-US", {
    hour: "2-digit",
    minute: "2-digit",
  })})</p>
        </div>
      </div>
      <div class="col-lg-12" style="margin-top: 15px; padding: 0 !important">
  <div class="sub-secciontitle" style="margin-bottom: 0 !important">
    <p class="subtitles-blancos" style="font-weight: bold; color: rgb(0, 123, 255);">
      Marca con una "✔" los Pokémon que te faltan
    </p>
  </div>
</div>
    `;

  // Renderizar las secciones
  eventData.secciones.forEach((seccion) => {
    let sectionHTML = `
        <div class="seccioncontainer" style="margin-top: 15px">
          <div class="titulo-icono">
            <div class="avatar">
              <img src="${seccion.icono}" alt="${seccion.titulo}" style="max-width: 46px; border-radius: 50%" />
            </div>
            <h3>${seccion.titulo}<br /><span>${seccion.subtitulo}</span></h3>
          </div>
      `;

    // Manejar secciones con items directos
    if (seccion.items) {
      sectionHTML += `
          <div class="imagenes-grid">
            ${seccion.items
              .map(
                (item, index) => `
                <div class="item item-pkmEvent ${item.raro ? "raro" : ""}" id="pokemon-${index}">
                  <div class="thumb thumb-pkmEvent">
                    <img src="${item.imagen}" alt="${item.nombre}" class="pokemon-image" />
                    ${
                      item.shiny
                        ? `<div class="shiny-icon">
                            <img src="../../assets/images/simbolos-incursiones/shiny.png" 
                                alt="shiny" class="shiny-hover" 
                                data-images='["${item.imagen}", "${item.shinyImage}"]' />
                          </div>
                          <div class="shiny-check">
                             <input type="checkbox" class="shiny-checkbox" data-nombre="${item.nombre}" />
                          </div>`
                        : ""
                    }
                    <div class="pokemon-name">${item.nombre}</div>
                  </div>
                </div>
              `
              )
              .join("")}
          </div>
        `;
    } else {
      sectionHTML += `<p class="no-data">No hay elementos disponibles para esta sección.</p>`;
    }

    sectionHTML += `</div>`;
    container.innerHTML += sectionHTML;
  });

  // Renderizar los banners antes de los bonus
  const bannersHTML = `
      <div class="footer-container">
        <div class="footer-banner">
          <span class="shiny-icon-circle">
            <img
              src="../../assets/images/simbolos-incursiones/shiny.png"
              alt="shiny icon"
              class="shiny-icon"
            />
          </span>
          <span>Posibilidad de Brillante</span>
        </div>
      </div>
    `;
  container.innerHTML += bannersHTML;

  // Renderizar los bonus
  if (eventData.bonus && eventData.bonus.length > 0) {
    const bonusHTML = `
        <div class="bonus-list">
          <div class="titulo-bonus" style="margin-bottom: 0 !important">
            <h3>Bonus del evento<br /></h3>
          </div>
          ${eventData.bonus
            .map(
              (bonus) => `
            <div class="bonus-item">
              <img src="${bonus.icono}" alt="Bonus" class="bonus-icon" />
              <p>${bonus.texto}</p>
            </div>
          `
            )
            .join("")}
        </div>
      `;
    container.innerHTML += bonusHTML;
  }
  applyShinyHoverEffects();
  applyCheckboxEffects();
}

function applyCheckboxEffects() {
  document.querySelectorAll(".shiny-checkbox").forEach((checkbox) => {
    const pokemonName = checkbox.dataset.nombre; // Identificar el Pokémon por su nombre

    // Restaurar estado desde localStorage
    const savedState = localStorage.getItem(`shiny-${pokemonName}`);
    if (savedState === "true") {
      checkbox.checked = true;
      checkbox.closest(".item-pkmEvent").style.backgroundColor = "#007bff"; // Fondo azul
    }

    checkbox.addEventListener("change", function () {
      const isChecked = this.checked;

      // Guardar estado en localStorage
      localStorage.setItem(`shiny-${pokemonName}`, isChecked);

      // Sincronizar con todos los checkboxes que tienen el mismo Pokémon
      document.querySelectorAll(`.shiny-checkbox[data-nombre="${pokemonName}"]`).forEach((cb) => {
        cb.checked = isChecked;
        const parentDiv = cb.closest(".item-pkmEvent");
        parentDiv.style.backgroundColor = isChecked ? "#007bff" : "#27292a";
      });
    });
  });
}


function preloadShinyImages(eventData) {
  eventData.secciones.forEach((seccion) => {
    if (seccion.items) {
      seccion.items.forEach((item) => {
        if (item.shiny) {
          const img = new Image();
          img.src = item.shinyImage; // Preload la imagen shiny
        }
      });
    }
  });
}

