function applyCheckboxEffects() {
  document.querySelectorAll(".shiny-checkbox").forEach((checkbox) => {
    const pokemonName = checkbox.dataset.nombre; // Identificar el Pokémon por su nombre

    // Restaurar estado desde localStorage
    const savedState = localStorage.getItem(`shiny-${pokemonName}`);
    if (savedState === "true") {
      checkbox.checked = true;
      checkbox.closest(".item-pkmEvent").style.backgroundColor = "#007bff"; // Fondo azul
    }

    checkbox.addEventListener("change", function () {
      const isChecked = this.checked;

      // Guardar estado en localStorage
      localStorage.setItem(`shiny-${pokemonName}`, isChecked);

      // Sincronizar con todos los checkboxes que tienen el mismo Pokémon
      document.querySelectorAll(`.shiny-checkbox[data-nombre="${pokemonName}"]`).forEach((cb) => {
        cb.checked = isChecked;
        const parentDiv = cb.closest(".item-pkmEvent");
        parentDiv.style.backgroundColor = isChecked ? "#007bff" : "#27292a";
      });
    });
  });
}


function preloadShinyImages(eventData) {
  eventData.secciones.forEach((seccion) => {
    if (seccion.items) {
      seccion.items.forEach((item) => {
        if (item.shiny) {
          const img = new Image();
          img.src = item.shinyImage; // Preload la imagen shiny
        }
      });
    }
  });
}

