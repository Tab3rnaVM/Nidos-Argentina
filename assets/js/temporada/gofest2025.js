function applyShinyHoverEffects() {
  const shinyIcons = document.querySelectorAll(".shiny-hover");
  shinyIcons.forEach((icon) => {
    const images = JSON.parse(icon.dataset.images); // Obtén las imágenes desde el data-images
    const pokemonImage = icon.closest(".thumb").querySelector(".pokemon-image"); // Encuentra la imagen principal del Pokémon
    if (pokemonImage) {
      icon.addEventListener("mouseover", () => {
        pokemonImage.src = images[1]; // Cambia a la imagen shiny
      });
      icon.addEventListener("mouseout", () => {
        pokemonImage.src = images[0]; // Vuelve a la imagen normal
      });
    }
  });
}

